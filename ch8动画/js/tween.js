/*    curEle:运动的元素
      target : 运动的目标,对象，可能包含多个方向
      duration :运动时间
      effect ：动画效果，默认为Linear
               支持数字，如1--> Linear , 2--> Bounce.easeIn ....
               支持数组，如["Bounce","easeIn"], 为Bounce.easeIn
    */


    var effectMap = {
        1:aniEffect.Linear,
        2:aniEffect.Bounce.easeIn //可补充全面
    }


    function move(curEle,target,duration,effect,callBack){
        var begin ={},change = {};
        // 获取各个方向上的初始值begin[key] 和 运动最大范围值change[key]
        for(var key in target){
            if(target.hasOwnProperty(key)){
                begin[key] = utils.css(curEle,key);
                change[key] = target[key] - begin[key];
            }
        }

        var time = 0;

        var finalEffect = aniEffect.Linear; //默认最终动画效果为匀速
        if(typeof effect=="number"){
            finalEffect =  effectMap[effect];
        }else if(effect instanceof Array){
            finalEffect = effect.length==2? aniEffect[effect[0]][effect[1]] : aniEffect[effect[0]];
        }else if(typeof effect=="function"){ //未传入动画效果，取默认匀速
            callBack = effect;
        }

        window.clearInterval(curEle.timer);
        curEle.timer = window.setInterval(function(){
            time+=10;
            if(time>=duration){
                window.clearTimeout(curEle.timer);
                utils.css(curEle,target);
                if(typeof callBack =="function"){
                    callBack();
                }

                return;
            }
            for(var key in target){
                if(target.hasOwnProperty(key)){
                    var curPostion = finalEffect(time,begin[key],change[key],duration);
                    utils.css(curEle,key,curPostion);

                }
            }

        },10)
    }

